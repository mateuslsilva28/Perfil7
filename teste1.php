  // Função para gerar índice aleatório sem repetição na sessão
  function getRandomUnusedIndex(max) {
    let usados = sessionStorage.getItem('indicesUsados');
    usados = usados ? JSON.parse(usados) : [];

    if (usados.length >= max) {
      alert('Você já viu todas as cartas! Reiniciando...');
      sessionStorage.removeItem('indicesUsados');
      usados = [];
    }

    let idx;
    do {
      idx = Math.floor(Math.random() * max);
    } while (usados.includes(idx));

    usados.push(idx);
    sessionStorage.setItem('indicesUsados', JSON.stringify(usados));
    return idx;
  }

  // Função para preencher o layout com os dados da carta
  function preencherCarta(carta) {
    const typeText = document.getElementById('typeText');
    const characterBanner = document.getElementById('characterBanner');
    const dicasList = document.getElementById('dicasList');
    const prefixo = (escapeHtml(carta.tipo) == "Pessoa" || escapeHtml(carta.tipo) == "Coisa") ? "uma" : "um";
    typeText.innerHTML = `Diga aos jogadores que sou ${prefixo} <strong>${escapeHtml(carta.tipo)}</strong>`;
    characterBanner.textContent = carta.item;

    dicasList.innerHTML = ''; // limpa dicas antigas
    carta.dicas.forEach(dica => {
      const li = document.createElement('li');
      li.textContent = dica;
      dicasList.appendChild(li);
    });
  }

  // Função simples para escapar HTML para evitar injeção
  function escapeHtml(text) {
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
  }

  // Evento do botão para gerar dica aleatória
  document.getElementById('btnGerar').addEventListener('click', () => {
    const idx = getRandomUnusedIndex(cartas.length);
    const carta = cartas[idx];
